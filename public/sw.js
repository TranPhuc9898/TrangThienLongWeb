// Push notifications
self.addEventListener('push', (event) => {
  const options = {
    body: event.data ? event.data.text() : 'Khuyến mãi mới từ Trang Mobile!',
    icon: '/images/ttl.png',
    badge: '/images/ttl.png',
    vibrate: [100, 50, 100],
    data: {
      dateOfArrival: Date.now(),
      primaryKey: 1
    }
  };

  event.waitUntil(
    self.registration.showNotification('Trang Mobile', options)
  );
});